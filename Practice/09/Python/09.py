print('Введите время прихода первого человека и время прихода второго человека через двоеточие')
s = input()
t = input()
b = int(s[-2] + s[-1])
d = int(t[-2] + t[-1])
s=s[0]+s[1]
t=t[0]+t[1]
if s[1] == ':':
  s = s[0]
if t[1] == ':':
  t = t[0]
a = int(s)
c = int(t)
if a == c and abs(b - d) <= 15 or abs(a - c) == 1 and abs(b - d - 60) <= 15:
  print('Встреча состоится!')
else:
  print('Встреча не состоится!')